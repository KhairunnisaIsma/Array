class Peta {
    String kota;
    int y,x;
 
    public Peta(String kota,int y,int x)
    {
        this.kota = kota;
        this.y = y;
        this.x = x;
    }

    public String getKota() {
        return kota;
    }

    public void setKota(String kota) {
        this.kota = kota;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

}
